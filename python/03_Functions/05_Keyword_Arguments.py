#Create a function to print product details (name, price, brand, rating).
def product_detail(name,price,brand,rating):
    print("Name of product:",name)
    print("Price of product:",price)
    print("Brand of product:",brand)
    print("Rating of product:",rating)

product_detail(name ="Eyeshadow palette",price = 300,brand = "Mars",rating = 4.5)
product_detail(name = "Lipstick",price = 100,brand = "Blue Heaven",rating = 4)